#Crie um programa que leia  de uma cidade e diga se ela começa ou não com o nome "SANTO".

cidade = str(input("Em que cidade você nasceu? ")).strip() #strip elimina os espaços

print(cidade[0:5].upper() == 'SANTO') #Confere se a cidade digitada é igual a "Santo".

